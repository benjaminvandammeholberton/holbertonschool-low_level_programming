#include "3-calc.h"
#include <stdio.h>
#include <stdlib.h>

/**
  * op_add - function that add two integers
  * @a: first integer
  * @b: second integer
  * Return: the result of the sum of a and b.
  */
int op_add(int a, int b)
{
return (a + b);
}

/**
  * op_sub - function that add two integers
  * @a: first integer
  * @b: second integer
  * Return: the result of the sum of a and b.
  */
int op_sub(int a, int b)
{
return (a - b);
}

/**
  * op_mul - function that add two integers
  * @a: first integer
  * @b: second integer
  * Return: the result of the sum of a and b.
  */
int op_mul(int a, int b)
{
return (a * b);
}

/**
  * op_div - function that add two integers
  * @a: first integer
  * @b: second integer
  * Return: the result of the sum of a and b.
  */
int op_div(int a, int b)
{
return (a / b);
}

/**
  * op_mod - function that add two integers
  * @a: first integer
  * @b: second integer
  * Return: the result of the sum of a and b.
  */
int op_mod(int a, int b)
{
return (a % b);
}
